int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    run();
}

void run() {
    int64_t Q, H, S, D, N;
    cin >> Q >> H >> S >> D >> N;
    int64_t min_per_liter = min(min(4 * Q, 2 * H), S);
    int64_t min_per_two_liter = min(2 * min_per_liter, D);
    cout << min_per_two_liter * (N / 2) + min_per_liter * (N % 2) << endl;
}
