#include <iostream>

using namespace std;

int main() {
  int one_year_in_seconds = 365 * 24 * 60 * 60;
  int two_years_in_seconds = 2 * one_year_in_seconds;
  int five_years_in_seconds = 5 * one_year_in_seconds;
  int ten_years_in_seconds = 10 * one_year_in_seconds;

  cout << one_year_in_seconds << endl;
  cout << two_years_in_seconds << endl;
  cout << five_years_in_seconds << endl;
  cout << ten_years_in_seconds << endl;
}
